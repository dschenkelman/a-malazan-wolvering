package phoneticket.android.model;

public class PostedArmChair {

	private int row;
	private int column;

	public PostedArmChair(int row, int column) {
		super();
		this.row = row;
		this.column = column;
	}

	public int getRow() {
		return row;
	}

	public int getColumn() {
		return column;
	}

}
